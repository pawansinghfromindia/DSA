#include <bits/stdc++.h>
using namespace std;

int main() {
      
/*
    Area  = length ×  breadth
Perimeter = 2 × (length+breadth)

Input
5 7
Output
Area = 35
Perimeter = 24
*/
  
  int length, breadth;
  cin >> length >> breadth;
  
  cout << "Area = " << length *  breadth << endl;
  cout << "Perimeter = " << 2 * (length+breadth) << endl;
  
}

