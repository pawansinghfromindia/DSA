#include <bits/stdc++.h>
using namespace std;

/*
Input :
5
Output :
**
**
**
**
**

Print n lines. Each line must contain exactly two stars ('**').

*/
int main() {
    
    int n; 
    cin >> n;
    
    for(int i=1; i<=n; i++){
        cout << "**" << endl;
    }
   
   
    return 0;
}